const regIP = /^(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$/;
export function validateIP(ip) {
    if (regIP.test(ip)) {
        return true;

    }
    else {
        alert("Please enter a valid IP address")
        return false;
    }
}